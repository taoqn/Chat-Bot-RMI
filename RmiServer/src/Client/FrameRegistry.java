    private void jDangKyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDangKyActionPerformed
        try {
            Registry r = LocateRegistry.getRegistry("localhost", 1099);
            server = (ServerInterFace) r.lookup("rmi://localhost/ABC");

            byte[] utf8Bytes = jNickName.getText().getBytes("UTF16");
            String nick = new String(utf8Bytes, "Cp1252");
            utf8Bytes = jHT.getText().getBytes("UTF16");
            String hoten = new String(utf8Bytes, "Cp1252");
            utf8Bytes = jDiaChi.getText().getBytes("UTF16");
            String diachi = new String(utf8Bytes, "Cp1252");

            if (!JUserName.getText().equals("") && !jPasswordRegistry.getText().equals("") && !nick.equals("") && !hoten.equals("") && !jSDT.getText().equals("") && !diachi.equals("")) {
                int y = Integer.parseInt(jYear.getValue().toString());
                int m = Integer.parseInt(jMonth.getValue().toString());
                int d = Integer.parseInt(jDay.getValue().toString());
                ClientInterFace client = new ClientImpl(JUserName.getText(), jPasswordRegistry.getText(), nick, hoten, d + "/" + m + "/" + y, jSDT.getText(), diachi);
                if (server.addAccountServer(client)) {
                    server = null;
                    JOptionPane.showMessageDialog(this, "Tài khoản đã tạo thành công !");
                    fc.jTaiKhoan.setText(JUserName.getText());
                    fc.jPasswordLogin.setText(jPasswordRegistry.getText());
                    fc.jBtnDangKy.setEnabled(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Tài khoản hay NickName đã bị trùng !");
                    JUserName.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin !");
            }
        } catch (RemoteException | NotBoundException ex) {
            JOptionPane.showMessageDialog(this, "Không thể kết nối tới Server !.");
            Logger.getLogger(FrameClient.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(FrameRegistry.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jDangKyActionPerformed

