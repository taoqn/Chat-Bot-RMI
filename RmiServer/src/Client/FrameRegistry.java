    /**
     * @param args the command line arguments
     */
    public void setDay() {
        int y = Integer.parseInt(jYear.getValue().toString());
        int m = Integer.parseInt(jMonth.getValue().toString());
        int d = Integer.parseInt(jDay.getValue().toString());
        switch (m) {
            case 2:
                if (y % 4 == 0 && d > 29) {
                    jDay.setValue(29);
                }
                if (y % 4 != 0 && d > 28) {
                    jDay.setValue(28);
                }
                break;
            case 4:
                if (d > 30) {
                    jDay.setValue(30);
                }
                break;
            case 6:
                if (d > 30) {
                    jDay.setValue(30);
                }
                break;
            case 9:
                if (d > 30) {
                    jDay.setValue(30);
                }
                break;
            case 11:
                if (d > 30) {
                    jDay.setValue(30);
                }
                break;
        }
    }

